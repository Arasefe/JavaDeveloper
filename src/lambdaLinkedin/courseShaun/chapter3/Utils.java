package lambdaLinkedin.courseShaun.chapter3;

public class Utils {
    public static Integer timesTwo(Integer x){
        return x*2;
    }

    public static boolean checkIfEven(Integer x){
        return x%2==0;
    }

    public static boolean isLongerThan5(String str){
        return str.length()>5;
    }
}
